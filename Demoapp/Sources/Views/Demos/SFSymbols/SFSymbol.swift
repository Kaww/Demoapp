import Foundation

struct SFSymbol: Identifiable {
    let name: String
    let category: SFSymbolCategory
    var id: String { name }
}

enum SFSymbolCategory: String, Hashable, CaseIterable, Identifiable {
    case all = "All"

    case connectivity = "Connectivity"
    case transport = "Transport"
    case accessibility = "Accessibility"
    case privacyAndSecurity = "Privacy & Security"
    case human = "Human"
    case home = "Home"
    case fitness = "Fitness"
    case nature = "Nature"
    case editing = "Editing"
    case textFormatting = "Text Formatting"
    case media = "Media"
    case keyboard = "Keyboard"
    case commerce = "Commerce"
    case time = "Time"
    case health = "Health"
    case shapes = "Shapes"
    case arrows = "Arrows"
    case indices = "Indices"
    case maths = "Math"

    var symbolName: String {
        switch self {
        case .all: return "square.grid.2x2"

        case .connectivity: return "antenna.radiowaves.left.and.right"
        case .transport: return "car.fill"
        case .accessibility: return ""
        case .privacyAndSecurity: return "lock"
        case .human: return "person.crop.circle"
        case .home: return "house"
        case .fitness: return "figure.run"
        case .nature: return "leaf"
        case .editing: return "slider.horizontal.3"
        case .textFormatting: return "textformat"
        case .media: return "playpause"
        case .keyboard: return "command"
        case .commerce: return "cart"
        case .time: return "timer"
        case .health: return "heart"
        case .shapes: return "square.on.circle"
        case .arrows: return "arrow.forward"
        case .indices: return "a.circle"
        case .maths: return "x.squareroot"
        }
    }

    var id: String { rawValue }
}

